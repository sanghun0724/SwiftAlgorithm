func minSubArrayLen(_ target: Int, _ nums: [Int]) -> Int {
    let size = nums.count
    var ans = Int.max
    var left = 0
    var sum = 0
    
    for i in 0..<size {
        sum += nums[i] //누적값 배열 채워나가는거
        while sum >= target {
            let result = i + 1 - left //true니까 길이값
            ans = min(ans,result) //그전의 result값이랑 비교하고 저장
            sum -= nums[left]//left값 뺴고 옮기기
            left += 1
        }
    }
    return 2

    }
