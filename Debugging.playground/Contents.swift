func generate(_ numRows: Int) -> [[Int]] {
     var res = [[Int]]()
    for i in 1...numRows {
      res+=[Array(repeating: 1, count: i)]
    }
    
    for i in 0..<numRows {
        for j in 0...i {
            if j != 0 && j != i {
                res[i][j] = res[i-1][j-1] + res[i-1][j]
            }
        }
    }
    return res
   }

