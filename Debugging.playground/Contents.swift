// Maximum Product of Two Elements in an Array
func maxProduct(_ nums: [Int]) -> Int {
    var max1 = nums[0]
    var max2 = Int.min
    for i in 1..<nums.count {
        if max1 < nums[i] {
            max2 = max1
            max1 = nums[i]
        } else if max2 < nums[i] {
            max2 = nums[i]
        }
    }
    return (max1-1) * (max2-1)
}
