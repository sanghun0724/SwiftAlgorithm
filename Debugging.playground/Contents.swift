func minTimeToVisitAllPoints(_ points: [[Int]]) -> Int {
    var cur = points[0]
    var timeVisiting = 0
    
    for i in 1..<points.count {
      var cur2 = points[i]
      var distanceX = abs(cur[0] - cur2[0])
      var distanceY = abs(cur[1] - cur2[1])
      cur = cur2
      timeVisiting = max(distanceX, distanceY)
    }
    return timeVisiting
}
