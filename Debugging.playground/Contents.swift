func twoSum(_ numbers: [Int], _ target: Int) -> [Int] {
    var start = 0
    var end  = numbers.count - 1
    var ans:[Int] = []
    
    while start < end {
        let sum = numbers[start] + numbers[end]
        if sum == target {
            ans.append(start + 1)
            ans.append(end + 1)
            return ans
        } else if sum > target {
            end-=1
        } else {
            start+=1
        }
    }
    return ans
    
    }
